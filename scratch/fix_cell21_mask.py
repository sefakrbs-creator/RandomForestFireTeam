lines.append('fig3.tight_layout()\n')
lines.append('hist_path = OUT_ROOT / "probability_histograms.png"\n')
lines.append('fig3.savefig(hist_path, dpi=150, bbox_inches="tight")\n')
lines.append('plt.show()\n')
lines.append('log(f"  Saved histograms: {hist_path}")\n')

